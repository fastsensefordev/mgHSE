package com.hs.model;
public class TotalInfo {
	/**
	 * 统计值
	 */
	private Integer count;
	/**
	 * 时间
	 */
	private String time;
	
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
}

	